echo -e " \e[0;32m==============================\e[0m"
echo -e "     \e[1;36mLimit Bandwidth Speed\e[0m"
echo -e " \e[0;32m==============================\e[0m"
echo -e " Status $sts"
echo -e "  1. Start Limit"
echo -e "  2. Stop Limit"
echo -e " Press CTRL+C to return"
read -rp " Please Enter The Correct Number : " -e num
if [[ "$num" = "1" ]]; then
